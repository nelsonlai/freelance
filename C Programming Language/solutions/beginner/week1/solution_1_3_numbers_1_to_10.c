/**
 * Solution 1.3: Numbers 1-10
 * Week 1 - Introduction to C Programming
 * 
 * Description: Create a program that prints the numbers 1 through 10
 */

#include <stdio.h>

int main() {
    printf("Numbers from 1 to 10:\n");
    for(int i = 1; i <= 10; i++) {
        printf("%d\n", i);
    }
    return 0;
}
