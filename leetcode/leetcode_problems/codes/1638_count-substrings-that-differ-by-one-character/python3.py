"""
Problem: Count Substrings That Differ by One Character
Difficulty: Medium
Tags: string, dp, hash

Approach: For each starting position in s and t, count substrings with exactly one difference
Time Complexity: O(n * m * min(n, m)) where n, m are lengths
Space Complexity: O(1)
"""

class Solution:
    def countSubstrings(self, s: str, t: str) -> int:
        result = 0
        
        for i in range(len(s)):
            for j in range(len(t)):
                diff = 0
                k = 0
                while i + k < len(s) and j + k < len(t):
                    if s[i + k] != t[j + k]:
                        diff += 1
                        if diff > 1:
                            break
                    if diff == 1:
                        result += 1
                    k += 1
        
        return result