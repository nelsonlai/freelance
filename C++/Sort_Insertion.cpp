/* Insertion Sort
Time Complexity
Case	Time Complexity	Explanation
Best Case	O(n)	Already sorted, no shifts needed
Average Case	O(n²)	Random order → average n/2 comparisons/insertions per element
Worst Case	O(n²)	Reversed array → maximum shifts

Space Complexity
O(1) → In-place sort (no extra array used)

https://visualgo.net/en/sorting
*/

#include <iostream>
using namespace std;

#include <iostream>
using namespace std;

// Function to perform Insertion Sort
void insertionSort(int arr[], int n) {
    for (int i = 1; i < n; i++) {
        int key = arr[i];      // Current element to be inserted
        int j = i - 1;

        // Move elements that are greater than key to one position ahead
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j--;
        }

        // Insert the key at the correct location
        arr[j + 1] = key;
    }
}

// Function to print the array
void printArray(int arr[], int n) {
    for (int i = 0; i < n; i++)
        cout << arr[i] << " ";
    cout << endl;
}

// Main function to test the sort
int main() {
    int arr[] = {29, 10, 14, 37, 13};
    int n = sizeof(arr) / sizeof(arr[0]);

    cout << "Original array: ";
    printArray(arr, n);

    insertionSort(arr, n);

    cout << "Sorted array: ";
    printArray(arr, n);

    return 0;
}